describe('buildAgentsRepo — lock contention', () => {
  it('two concurrent builds on the same repo: both succeed, state is consistent', async () => {
    const repoDir = path.join(tmpDir, 'contention-repo');
    fs.mkdirSync(repoDir);
    setupMinimalRepo(repoDir);

    // Launch both builds simultaneously
    const [r1, r2] = await Promise.all([
      buildAgentsRepo(repoDir, { force: true, yes: true }),
      buildAgentsRepo(repoDir, { force: true, yes: true }),
    ]);

    expect(Array.isArray(r1.warnings)).toBe(true);
    expect(Array.isArray(r2.warnings)).toBe(true);

    // State file should exist and be valid JSON after both complete
    const statePath = path.join(repoDir, '.chalk', '.state.json');
    expect(fs.existsSync(statePath)).toBe(true);
    const state = JSON.parse(fs.readFileSync(statePath, 'utf8')) as Record<string, unknown>;
    expect(typeof state['manifest']).toBe('object');
  }, 15_000);
});

